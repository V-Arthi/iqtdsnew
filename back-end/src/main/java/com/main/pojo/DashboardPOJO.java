package com.main.pojo;

public class DashboardPOJO {

	private long conditions;

	private long runs;

	private long jobs;

	private long ediFiles;

	private long onlineClaims;

	private long mappings;

	private long extracts;

	public DashboardPOJO() {
	}

	public long getConditions() {
		return conditions;
	}

	public void setConditions(long conditions) {
		this.conditions = conditions;
	}

	public long getRuns() {
		return runs;
	}

	public void setRuns(long runs) {
		this.runs = runs;
	}

	public long getJobs() {
		return jobs;
	}

	public void setJobs(long jobs) {
		this.jobs = jobs;
	}

	public long getEdiFiles() {
		return ediFiles;
	}

	public void setEdiFiles(long ediFiles) {
		this.ediFiles = ediFiles;
	}

	public long getOnlineClaims() {
		return onlineClaims;
	}

	public void setOnlineClaims(long onlineClaims) {
		this.onlineClaims = onlineClaims;
	}

	public long getMappings() {
		return mappings;
	}

	public void setMappings(long mappings) {
		this.mappings = mappings;
	}

	public long getExtracts() {
		return extracts;
	}

	public void setExtracts(long extracts) {
		this.extracts = extracts;
	}

}
